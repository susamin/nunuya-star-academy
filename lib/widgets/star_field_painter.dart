  @override
  void paint(Canvas canvas, Size size) {
    for (final s in _stars) {
      canvas.drawCircle(
        Offset(size.width * s[0], size.height * s[1]),
        s[2],
        Paint()..color = Color.fromRGBO(255, 255, 255, s[3] / 255),
      );
    }
  }

  @override
  bool shouldRepaint(covariant CustomPainter old) => false;
}
